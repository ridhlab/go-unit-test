package mathOperation

func Sum(num1 int, num2 int) int {
	res := num1 + num2
	return res
}

func Substraction(num1 int, num2 int) int {
	res := num1 - num2
	return res
}

func Multiple(num1 int, num2 int) int {
	res := num1 * num2
	return res
}

func Divide(num1 int, num2 int) int {
	res := num1 / num2
	return res
}
